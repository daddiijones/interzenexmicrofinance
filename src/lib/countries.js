const COUNTRY_BY_CODE = new Map(COUNTRIES.map((c) => [c.code, c]));

export function getCountry(code) {
  return COUNTRY_BY_CODE.get(code) || null;
}

export function getCountryCurrency(code) {
  return COUNTRY_BY_CODE.get(code)?.currency || "USD";
}

// Renders a country's ISO code as its flag emoji, e.g. "US" -> 🇺🇸
export function countryFlag(code) {
  if (!code || code.length !== 2) return "";
  const base = 0x1f1e6 - "A".charCodeAt(0);
  return String.fromCodePoint(
    ...code.toUpperCase().split("").map((c) => base + c.charCodeAt(0))
  );
}
